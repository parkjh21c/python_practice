# 계산 함수 모음

class calculate:
  # 덧셈
  def add(num1 : any, num2 : any):
    answer = num1 + num2
    return answer
  
  # 뺄셈
  def minus(num1 : any, num2 : any):
    answer = num1 - num2
    return answer

  # 곱셈
  def multiplication(num1 : any, num2 : any):
    answer = num1 * num2
    return answer
  
  # 나눗셈
  def division(num1 : any, num2 : any):
    answer = num1 / num2
    return answer