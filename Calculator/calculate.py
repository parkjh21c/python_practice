# 계산 함수 모음

class calculate:
  # 덧셈
  def add(self, num1 : any, num2 : any):
    answer = int(num1) + int(num2)
    return answer
  
  # 뺄셈
  def minus(self, num1 : any, num2 : any):
    answer = int(num1) - int(num2)
    return answer

  # 곱셈
  def multiplication(self, num1 : any, num2 : any):
    answer = int(num1) * int(num2)
    return answer
  
  # 나눗셈
  def division(self, num1 : any, num2 : any):
    answer = int(num1) / int(num2)
    return answer